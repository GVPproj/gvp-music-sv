	<p class="release-text">
		"I'd like to be as un-mysterious as I possibly can," Graham Van Pelt says. He's about to
		release Time Travel, his first record in the four years since he moved from Montreal to Toronto,
		and he's no longer Miracle Fortress, or Inside Touch, or any of the aliases he's used to record
		over the years. He's just Graham Van Pelt.
		<br /><br />
		With the perspective of leaving a longtime home, of watching old friends change and familiar places
		become unrecognizable, came a recommitment to an emotional honesty in Van Pelt's songwriting. Time
		Travel's eight songs are a tangle of friendships and feelings. Moving backwards and forwards in time,
		they occupy a space of elegant melancholy.
		<br /><br />
		Immersing himself in the work of house music legends like Larry Heard, Vincent Floyd and Maurizio
		and the fragile disco of Arthur Russell, along with contemporaries like Jessy Lanza and Kelly Lee
		Owens, Van Pelt built the album from the bottom up, rooting every track in the crude sequencer of
		the Roland SH-101 synth, a decades-old dance music totem. The result are melodies that are simple
		but affecting, anchored to deep, wandering basslines. Time Travel was engineered by twin brothers
		Mark and Matt Thibideau, whose techno roots deepened the grooves throughout the record.
		<br /><br />
		There's a comfort to operating in the world of dance music, which is, in Van Pelt's words, more of
		a "team sport" than the competitive atmosphere of indie rock. "I feel like one person participating
		in a community," he says, "and less of a person trying to rise above." The album is also Van Pelt's
		first release on Arbutus, which he describes as his "dream outcome" for the record. "Their catalog
		intersects at a place that feels like home to me," he says. "Music for late night, atmospheric music,
		really honest and unvarnished truthful stuff that never compromises."
		<br /><br />
		Time Travel is a renewal of Van Pelt's vows with dance music, and with the genre's pulse of synthesized
		melancholy. It's a heartrending rush, as emotionally direct as a sweaty, jaw-clenching 3am hug.
	</p>
