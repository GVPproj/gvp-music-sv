<div class="release">
	<h2>Time Travel</h2>
	<h3>2018, Arbutus Records</h3>
	<div class="release-block">
		<img
			class="album-cover"
			src="https://f4.bcbits.com/img/a1357217104_10.jpg"
			alt="Time Travel album cover"
		/>
		<BandcampPlayer
			albumId="1538922128"
			albumUrl="https://grahamvanpelt.bandcamp.com/album/time-travel"
			albumTitle="Time Travel by Graham Van Pelt"
